package sifely;


public class AlexaParam {

    /**
     * 数据主体，alexa的原生数据
     */
    private String dtBody;

    /**
     * 数据来源
     */
    private String source;

    public String getDtBody() {
        return dtBody;
    }

    public void setDtBody(String dtBody) {
        this.dtBody = dtBody;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }
}
